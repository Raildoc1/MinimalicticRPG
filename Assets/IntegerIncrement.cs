using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class IntegerIncrement : StateMachineBehaviour
{

    public string integerName = "";
    public string boolName = "";
    public string limitName = "";

    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {

        var hash = Animator.StringToHash(integerName);

        animator.SetInteger(hash, animator.GetInteger(hash) + 1);

        if (animator.GetInteger(hash) >= animator.GetInteger(limitName))
        {
            animator.SetBool(boolName, true);
        }
    }

